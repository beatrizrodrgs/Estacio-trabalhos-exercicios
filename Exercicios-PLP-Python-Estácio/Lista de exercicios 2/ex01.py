# 1) Faça um programa para calcular a media de um aluno. O programa deve receber:
# o nome do aluno e 3 notas pela função input(); e no final deve imprimir a média desse aluno

nomeAluno = input ("Digite o nome do Aluno:")
nota1 = input ("Digite a Primeira Nota:")
nota2 = input ("Digite a Segunda Nota:")
nota3 = input ("Digite a Terceira Nota:")

media = (nota1 + nota2 + nota3)
print ("A Média do Aluno:") , nomeAluno
print ("Foi:") , media
